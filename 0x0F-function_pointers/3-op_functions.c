#include "3-calc.h"

/**
  * op_add - adds two int
  * @a: 1st No
  * @b: 2nd No
  *
  * Return: summation
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - subs two int
  * @a: 1st No
  * @b: 2nd No
  *
  * Return: subtraction
  */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - multiplies two int
  * @a: 1st No
  * @b: 2nd No
  *
  * Return: multiplication
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - divides two int
  * @a: 1st No
  * @b: 2nd No
  *
  * Return: division
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - get the modulo of two int
  * @a: 1st No
  * @b: 2nd No
  *
  * Return: modulo
  */

int op_mod(int a, int b)
{
	return (a % b);
}
